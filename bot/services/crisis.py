"""Детектор кризисных ситуаций — ключевые слова на казахском и русском."""

_CRISIS_KEYWORDS_KZ = [
    "өзіме қол жұмсау", "өлгім келеді", "өмірге қиғым келеді",
    "өзімді өлтіру", "тіршілік жоқ", "өлсем деймін",
    "қолымды кесу", "дәрі ішу", "өзін-өзі өлтіру",
]

_CRISIS_KEYWORDS_RU = [
    "хочу умереть", "покончить с собой", "самоубийств",
    "суицид", "не хочу жить", "нет сил жить",
    "вскрыть вены", "прыгнуть", "передозировка",
    "таблетки выпить", "покончу", "убью себя",
]


def is_crisis(text: str) -> bool:
    t = text.lower()
    for kw in _CRISIS_KEYWORDS_KZ + _CRISIS_KEYWORDS_RU:
        if kw in t:
            return True
    return False
